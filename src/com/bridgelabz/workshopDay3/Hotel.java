package com.bridgelabz.workshopDay3;

public class Hotel {
        String name;
        int weekdayRate;
        int weekendRate;
        int rewardWeekRate;
        int rewardWeekendRate;
        int rating;



        public void setName(String lakewood) {
                this.name=name;
                setName(lakewood);

        }

        public void setWeekdayRate(int i) {
                this.weekdayRate=weekdayRate;
                setWeekdayRate(i);
        }

        public void setRewardWeekdayRate(int i) {
                this.rewardWeekRate=rewardWeekRate;
                setRewardWeekdayRate(i);

        }

        public void setWeekendRate(int i) {
                this.weekendRate=weekendRate;
                setWeekendRate(i);
        }

        public void setRewardWeekendRate(int i) {
                this.rewardWeekendRate=rewardWeekendRate;
                setRewardWeekendRate(i);
        }

        public void setRating(int i) {
                this.rating=rating;
                setRating(i);
        }
}
